// Function Declaration
function sayGoodMorning() {
  console.log("Good morning!");
}

// Function Expression
const sayGoodNight = function () {
  console.log("Good night!");
};

sayGoodMorning();
sayGoodNight();

function sum(a, b) {
  return a + b;
}

console.log(sum(1, 2));
console.log(sum(1, 3, 6, 3));
console.log(sum(9));
